    public int[][][] insertPiece(int[][][] piece, int i, int j, int k){
        int[][][] tempBox = this.box;
        for(int x = 0; x < 4; x++){
            for(int y = 0; y < 4; y++){
                for(int z = 0; z < 4; z++){
                    tempBox[x][y][z] = tempBox[x + i][y + j][z + k] + piece[x][y][z];
                }
            }
        }
        return tempBox;
    }

